In PHP, keywords (e.g. if, else, while, echo, etc.), classes, functions, and user-defined functions are not case-sensitive.
However; all variable names are case-sensitive!

<?php
$txt = "W3Schools.com";
echo "I love $txt!"; //php in double quote
?>
